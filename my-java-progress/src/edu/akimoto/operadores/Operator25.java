package edu.akimoto.operadores;

public class Operator25 {
    public static void main(String[] args) {
        int number1 = 3;
        int number2 = 2;

        if(number1 > number2)
            System.out.println("Número 1 é maior que o número2");

        if(number1 < number2)
            System.out.println("Número 1 é menor que o número 2");

        if(number1 >= number2)
            System.out.println("Número 1 é maior ou igual ao número 2");

        if(number1 <= number2)
            System.out.println("Número1 é menor ou igual ao número 2");

        if(number1 != number2)
            System.out.println("Número 1 é diferente do número 2");
    }
}
