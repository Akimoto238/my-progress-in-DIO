public class Operator15 {
    public static void main(String[] args) {
        int number = 5;

        number = - number;

        System.out.println(number);

        number = number * -1;

        System.out.println(number);
        //É necessário multiplicar o número por -1 para que se torne positivo novamente.
    }
}
